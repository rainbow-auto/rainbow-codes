inline i64 qry(i64 n) {
	if (n <= 1) return n;
	i64 res = 0;
	for (i64 l = 1, r; l <= n; l = r + 1) {
		r = n / (n / l);
		res += (n / l) * (r - l + 1) * (r + l) / 2;
	}
	return res;
}

